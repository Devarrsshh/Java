public class cwh_10_resulting_data_type {
    public static void main(String[] args) {
        /*
         * byte x = 5;
         * int y = 6;
         * short z = 8;
         * int a = y + z;
         * float b = 6.54f + x;
         * System.out.println(b);
         */

        // Increment and Decrement Operators
        int i = 56;
        int j = 67;
        int c = ++j;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int y = 7;
        System.out.println(++y * 8);
        char ch = 'a';
        System.out.println(++ch);
    }
}